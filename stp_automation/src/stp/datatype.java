package stp;

public class datatype {

	public static void main(String[] args) {
		// numeric datatype

		// integer datatype
		byte a = 5;
		short b = 10;
		int h = 15;
		long k = 20;

		// floating datatype
		float c = 30f;
		double d = 40554.866545;

		// non-numeric datatype

		char e = 45;
		boolean g = e > 44;
		System.out.println(g);

		// non primitive datatype
        // string is the collection of ,multiple character
		String i = "hk";
		// object formate
		// datatype variable = new datatype ("hk")--stands for
		String j = new String("hk");

	}

}
